public class Primitives {

    public static void main(String[] args) {

        //Primitives data types
        int rno = 45;
        float marks = 55.36f;
        char cah = 'c';
        boolean isTrue = false ; //or true
        long longNum = 4567891236457988787L;
        double dd = 8794531.363989;

        System.out.println("int "+rno);
        System.out.println("float "+marks);
        System.out.println("char "+cah);
        System.out.println("boolean "+isTrue);
        System.out.println("long "+longNum);
        System.out.println("Double "+dd);

    }
    
}
